#include <stdio.h>
/* Запишемо таблицю множення до 5 у файл*/
#define N 5

int main(){
   int i,j;
   FILE *fp;
   fp=fopen("matrix.txt","w");
  /*Відкриття файлу на диску для запису. Якщо він не існує, то створюється автоматично*/
  for(i=1;i<=N;i++){
    for(j=1;j<=N;j++){ 
/*Запис даних до файлу: Перший аргумент – вказівник на файл,інші такі ж як і для команди printf */
 	fprintf(fp,"%d%c",i*j,((j==N)?'\n':'\t'));}
}
fprintf(fp,"\n");
fclose(fp); /*Закриття файлу*/
}
